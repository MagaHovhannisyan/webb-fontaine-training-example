package information;

public @interface Annotations {
    /*
    Build in ANNOTATION EXAMPLES:
    * Override
    * Deprecated - used when given method() is not recommended to use and it has improved version
    * SuppressWarnings - used when we want to ignore warnings
     */

    /*
    When creating your Annotation

    RETENTION policy types:
    * Source
    * Class
    * Runtime (used by us)

    TARGET types:
    * Element type (method, class, field ...) - defies when we can use that annotation

    INHERITED - if Parent has that annotation, child has as well

    DOCUMENTED - available in java docs

     */
}
